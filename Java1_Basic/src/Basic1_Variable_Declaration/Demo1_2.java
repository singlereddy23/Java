package Basic1_Variable_Declaration;

public class Demo1_2
{
	public static void main(String[] args) 
	{
		//DataType Variable_Name = Value;

		Boolean isMarried = true;	//true or false
		System.out.println(isMarried);

		char value = '$';	//One Character with in Single Codes
		System.err.println(value);
		
		int Salary = 20000;	//Numbers
		System.out.println(Salary);
		
		float rate = 2.5f;	//Decimal value at end mark f
		System.out.println(rate);
		
		double rates = 2.554545;	//Decimal values
		System.out.println(rates);
		
		String Name = "Kamal23";	//Combination of Both Alpha, Alphanumeric & numbers with in Double Codes
		System.out.println(Name);
	}
}